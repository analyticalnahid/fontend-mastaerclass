
/* Logical AND (&&)
 The logical AND operator returns true if both operands are true, and false if 
 either operand is false.
*/

console.log(true && true);  // true
console.log(true && false);  // false
console.log(false && true);  // false
console.log(false && false);  // false


/* Logical OR (||)
 The logical OR operator returns true if either operand is true, and false 
 if both operands are false.
*/

console.log(true || true);  // true
console.log(true || false);  // true
console.log(false || true);  // true
console.log(false || false);  // false

/* Logical NOT (!)
 The logical NOT operator returns the opposite boolean value of the operand.
*/

console.log(!true);  // false
console.log(!false);  // true
