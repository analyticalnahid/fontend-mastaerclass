

// Addition (+): Adds two numbers and returns the result. 
let x1 = 5;
let y1 = 7;
let z1 = x1 + y1;  // z1 is 12


// Subtraction (-): Subtracts two numbers and returns the result.
let x2 = 5;
let y2 = 7;
let z2 = y2 - x2;  // z2 is 2


// Multiplication (*): Multiplies two numbers and returns the result.
let x3 = 5;
let y3 = 7;
let z3 = x3 * y3;  // z3 is 35


// Division (/): Divides one number by another and returns the result. 
let x4 = 8;
let y4 = 4;
let z4 = x4 / y4;  // z4 is 2


// Modulus (%): Returns the remainder after dividing one number by another.
let x5 = 8;
let y5 = 4;
let z5 = x5 % y5;  // z is 0


// Exponentiation ()**: Raises one number to the power of another and returns the result. 
let x6 = 2;
let y6 = 3;
let z6 = x6 ** y6;  // z is 8


// The increment operator (++) increments numbers
let a1 = 5;
a1++;
let b1 = a1; 
console.log(b1);  // 6


// The decrement operator (--) decrements numbers
let a2 = 5;
a2--;
let b2 = a2;
console.log(b2);  // 4