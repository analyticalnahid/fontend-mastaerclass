
// In JavaScript, the conditional operator (also known as the ternary operator) 
// is a way to specify a conditional expression. It is represented by the ? symbol

let x1 = 10;
let y1 = 20;

let max1 = (x1 > y1) ? x1: y1;
console.log(max1);  // 20


let x2 = 10;
let y2 = 20;

let max2 = (x2 > y2) ? x2 : y2;
console.log(max2);  // 20



