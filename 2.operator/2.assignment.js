/*   JavaScript Assignment Operators  */


// = : Simple assignment operator, assigns values from right side operands to left side operand.
let x1 = 10; // x1 is now 10


// += : Add AND assignment operator, adds right operand to the left operand and then assigns the result to left operand.
let x2 = 10;
let y2 = 5;

x2 += y2; // x2 is now 15


// -= : Subtract AND assignment operator, subtracts right operand from the left operand and then assigns the result to left operand.
let x3 = 10;
let y3 = 5;

x3 -= y3; // x3 is now 5


// *= : Multiply AND assignment operator, multiplies right operand with the left operand and then assigns the result to left operand.
let x4 = 10;
let y4 = 5;

x4 *= y4; // x4 is now 50


// /= : Divide AND assignment operator, divides left operand with the right operand and then assigns the result to left operand.
let x5 = 10;
let y5 = 5;

x5 /= y5; // x is now 2


// %= : Modulus AND assignment operator, takes modulus using two operands and then assigns the result to left operand.
let x6 = 10;
let y6 = 5;

x6 %= y6; // x is now 0


// **= : Exponentiation AND assignment operator, performs exponential (power) calculation on operators and then assigns value to the left operand.
let x7 = 10;
let y7 = 5;

x7 **= y7; // x is now 100000



