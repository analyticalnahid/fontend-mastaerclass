

/* In JavaScript, type conversion (or type casting) is the process of converting a 
value from one data type to another. */


// Number() function : This function can be used to convert a string or a boolean to a number.
console.log(Number('123')); // Output: 123
console.log(Number(true)); // Output: 1


// String() function : This function can be used to convert any value to a string.
console.log(String(123)); // Output: "123"
console.log(String(true)); // Output: "true"

(123).toString()
(true).toString()

// Boolean() function : This function can be used to convert any value to a boolean.
console.log(Boolean(0)); // Output: false
console.log(Boolean('hello')); // Output: true







