/*
Template literals are a new feature in JavaScript that allow you to create 
strings that include expressions. They are denoted by backticks (`) instead of 
single or double quotes.    */

// String concatenation
let firstName = "Nahidul";
let lastName = "Islam";
console.log(`My name is ${firstName} ${lastName}.`);

// Arithmetic expressions
let x = 3;
let y = 4;
console.log(`The sum of ${x} and ${y} is ${x + y}.`);

// Multi-line strings
console.log(`This is an example of
a multi-line string created
using template literals.`);



