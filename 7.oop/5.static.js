

// In JavaScript, a static method is a method that is associated with a class rather 
// than an instance of the class. This means that you can call a static method directly 
// on the class, without needing to create an instance of the class first.


class MyClass {
    static staticMethod() {
        console.log("This is a static method.");
    }
}

MyClass.staticMethod(); // Output: "This is a static method."
